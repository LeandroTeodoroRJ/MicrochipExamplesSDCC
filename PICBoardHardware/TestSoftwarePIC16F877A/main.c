#include <16F877.h>              //include do 16F877
#include <string.h>
#include <16f8x_registradores.h>//MAPEAMENTO DE MEÓRIA DOS REGISTRADORES
                                                    

//****************************************************************************
//                    CONFIGURAÇŐES DO COMPILADOR
//****************************************************************************

#use fast_io(a)                  //modo de entrada e saida de dados que
#use fast_io(b)                  //o compilador nao configura o tris

#use delay(clock=20000000)        //clock usual
#fuses HS,NOLVP,NOWDT,PUT,NOPROTECT

//****************************************************************************
//                                BIOS
//****************************************************************************
//****************************  ENTRADAS  ************************************

//*****************************  SAÍDAS  *************************************

//****************************************************************************
//                        CONSTANTES E VARIÁVEIS
//****************************************************************************


//***************************************************************************
//                               DRIVES
//***************************************************************************


//****************************************************************************
//                       Rotinas da interrupcao
//****************************************************************************



//****************************************************************************
//                           INTERRUPÇŐES
//****************************************************************************


//****************************************************************************
//                             Sub-rotinas
//****************************************************************************

//****************************************************************************
//                          Rotina Principal
//****************************************************************************

void main(){
//********************** CONFIGURAÇŐES INICIAIS *****************************
      setup_timer_0(rtcc_internal); //clock interno, prescaler 1:2
      setup_timer_1(t1_internal|t1_div_by_8); 
      setup_wdt(wdt_288ms);
      disable_interrupts(global);
      bit_set(option_reg,7);    //sem pull-up

//    Configuraçăo do tris
//    Bit porta: 76543210
      tris_a = 0b00000000;
      tris_b = 0b00000000;
      tris_c = 0b00000000;
      tris_d = 0b00000000;
      tris_e = 0b00000000;      
//******************** INICIALIZAÇĂO DE VARIÁVEIS ***************************
      
//****************************************************************************
//                             INICIO DO LOOP
//****************************************************************************
      while(1){  
         restart_wdt();
         output_A(0xff);
         output_B(0xff);
         output_C(0xff);
         output_D(0xff);
         output_high(pin_e0);
         output_high(pin_e1);
         output_high(pin_e2);
         delay_ms(5000);
         output_A(0x00);
         output_B(0x00);
         output_C(0x00);
         output_D(0x00);
         output_low(pin_e0);
         output_low(pin_e1);
         output_low(pin_e2);
         delay_ms(5000);
      }
//********************************* FIM **************************************
}


















































